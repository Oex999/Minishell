#include "../includes/minishell.h"

int	ft_putenv(t_env **env)
{
	int	i;

	i = 0;
	while ((*env)->env[i])
	{
		ft_printf("%s\n", (*env)->env[i]);
		i++;
	}
	return (1);
}
